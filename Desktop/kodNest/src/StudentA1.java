public class StudentA1 {
    private int id;
    private String name;
    private int marks;
    public void setterId(int id){
        this.id=id;
    }
    public void setterName(String name){
        this.name=name;
    }
    public void setterMarks(int marks){
        this.marks=marks;
    }
    public int getterId(){
        return id;
    }
    public String getterName(){
        return name;
    }
    public int getterMarks(){
        return marks;
    }



}
